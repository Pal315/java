class Calculator {
    public static void main(String args[]) {
	int num1 = 132;
	int num2 = 20;
        int sum = num1+num2;
        int diff = num1-num2;
        int mult = num1*num2;
        int div = num1/num2;
        int mod = num1%num2;
        System.out.println("The addition of numbers is : " +sum );
        System.out.println("The subtraction of numbers is : " +diff );
        System.out.println("The multiplication of numbers is : " +mult );
        System.out.println("The division of numbers is : " +div );
        System.out.println("The modulo of numbers is : " +mod );
    }
}